    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblInst.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(rootPane, "Selecione a linha a ser excluída.");
        } else {
            DefaultTableModel tabelaInst = (DefaultTableModel) tblInst.getModel();
            tabelaInst.removeRow(linhaSelecionada);
            JOptionPane.showMessageDialog(this, "Instituição excluída com sucesso.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

